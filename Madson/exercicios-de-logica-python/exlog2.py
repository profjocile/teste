#Faça um algoritmo para ler três números e imprimir a soma, média e produto dos números lidos.
print("digite o primeiro número:")
n1 = float(input())
print("digite o segundo número:")
n2 = float(input())
print("digite o terceiro número:")
n3 = float(input())

